CREATE TABLE tb_product_type (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "name" varchar(50) NOT NULL,
    parent_id integer,
    "status" integer,
    icon_url varchar(255),
    mark varchar(255),
    part_type_code varchar(50)
);

CREATE TABLE tb_product (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    model varchar(100),
    manufacturer varchar(100),
    remark varchar(255),
    protocol_id integer,
    product_type_id integer,
    product_key varchar(50),
    icon varchar(255)
);

CREATE TABLE tb_device (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id integer,
    device_name varchar(100),
    device_key varchar(50),
    device_secret varchar(100),
    online boolean,
    active_status boolean,
    third_device_id varchar(100),
    create_time timestamp,
    update_time timestamp
);

CREATE TABLE tb_icon (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name varchar(100),
    path varchar(255)
);

CREATE TABLE tb_model_property (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id integer,
    product_type_id integer,
    identifier varchar(50),
    "name" varchar(100),
    icon_id integer,
    data_type integer,
    access_mode varchar(10),
    param_type integer,
    custom boolean,
    parent_id integer,
    remark varchar(255),
    unit varchar(20),
    unit_name varchar(50),
    max bigint,
    min bigint,
    step bigint,
